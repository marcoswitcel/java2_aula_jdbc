    private void loadTabelaAutores(Livro livro) throws Exception {
        DefaultTableModel modelo = (DefaultTableModel) tAutor.getModel();
        modelo.setNumRows(0);
        
        for (Autor autor : livro.getAutores()) {
            String [] linha = {
                "" + autor.getAutor_id(),
                autor.getNome()
            };
            modelo.addRow(linha);
        }
    }
    
    public void limpaCampos() {
        iID.setText("");
        iTitulo.setText("");
        iAno.setText("");
        iDescricao.setText("");
        
        DefaultTableModel modelo = (DefaultTableModel) tAutor.getModel();
        modelo.setNumRows(0);
        // Muda para insert
        this.mode = "INS";
    }

    private void loadComboBox() {
        try {
            DefaultComboBoxModel model = new DefaultComboBoxModel(new Vector(editoraDAO.findAll()));
            cbEditora.setModel(model);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void loadTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        modelo.setNumRows(0);

        try {
            for (Livro livro : livroDAO.findAll()) {
                String[] linha = {
                    "" + livro.getLivro_id(),
                    livro.getEditora().getNome(),
                    livro.getTitulo(),
                    "" + livro.getAno()
                };
                modelo.addRow(linha);
            }
        } catch (Exception ex) {
            Logger.getLogger(LivroForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

