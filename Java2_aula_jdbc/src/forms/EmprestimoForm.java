    private void deletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarActionPerformed
        Emprestimo emp = new Emprestimo();
        try {
            emp.setEmprestimo_id(Integer.parseInt(cod_Livro.getText()));
            emprestimoDAO.delete(emp);
            
        } catch (Exception ex) {
            emp.setEmprestimo_id(0);
        }
        loadTabela();
        loadComboBox();
        limparCampos();
    }//GEN-LAST:event_deletarActionPerformed

    public void limparCampos() {
        cod_Livro.setText("");
        data_Retirada.setText("");
        data_Dev.setText("");
        data_Ent.setText("");
        status.setText("");

        this.mode = "INS";
    }

    public Date data_banco(String data) {
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        try {
            return format.parse(data);
        } catch (Exception ex) {
            Logger.getLogger(EmprestimoForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }

    public void loadComboBox() {
        try {
            //PROGRAMAR A BUSCA DE DADOS
            DefaultComboBoxModel model1 = new DefaultComboBoxModel(new Vector(estudanteDAO.findAll()));
            DefaultComboBoxModel model2 = new DefaultComboBoxModel(new Vector(livroDAO.findAll()));
            jComboBox1.setModel(model2);
            jComboBox2.setModel(model1);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    public void loadTabela() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);

        try {
            for (Emprestimo emp : emprestimoDAO.findAll()) {
                String entrega = (emp.getData_entrega() != null) ? emp.getData_entrega().toString() : "Não entregou";
                String[] row = {
                    "" + emp.getEmprestimo_id(),
                    emp.getLivro().getTitulo(),
                    emp.getEstudante().getNome(),
                    emp.getData_retirada().toString(),
                    emp.getData_devolucao().toString(),
                    entrega,
                    emp.getStatus()
                };
                model.addRow(row);
            }
        } catch (Exception ex) {
            System.out.println("Deu ruim no load table" + ex.getMessage());
            Logger.getLogger(EmprestimoForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

