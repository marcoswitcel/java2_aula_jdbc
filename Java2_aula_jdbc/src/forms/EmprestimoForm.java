    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        Emprestimo emp = new Emprestimo();
        try {
            emp.setEmprestimo_id(Integer.parseInt(cod_Livro.getText()));
        } catch (Exception ex) {
            emp.setEmprestimo_id(0);
        }
        emp.setLivro((Livro) jComboBox1.getSelectedItem());
        emp.setEstudante((Estudante) jComboBox2.getSelectedItem());
        emp.setData_devolucao(data_banco(data_Dev.getText()));
        emp.setData_entrega(data_banco(data_Ent.getText()));
        emp.setData_retirada(data_banco(data_Retirada.getText()));
        emp.setLivro((Livro) jComboBox1.getSelectedItem());
        emp.setEstudante((Estudante) jComboBox2.getSelectedItem());
        emp.setStatus(status.getText());

        try {
            if (mode.equals("INS")) {
                emprestimoDAO.save(emp);
            } else if (mode.equals("UPD")) {
                System.out.println("cheguuei");
                emprestimoDAO.update(emp);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            System.out.println(ex.getMessage());
        }
        loadComboBox();
        loadTabela();
        limparCampos();
    }//GEN-LAST:event_salvarActionPerformed

