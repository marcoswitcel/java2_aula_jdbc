    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();

        cod_Livro.setText((String) jTable1.getValueAt(row, 0));
        try {
            Emprestimo emp = emprestimoDAO.findById(Integer.parseInt((String) jTable1.getValueAt(row, 0)));
            jComboBox1.getModel().setSelectedItem(emp.getLivro());
            jComboBox2.getModel().setSelectedItem(emp.getEstudante());
        } catch (Exception ex) {
            System.out.println("Erro no combo box" + ex.getMessage());
            Logger.getLogger(EmprestimoForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        data_Retirada.setText(data_br());
        data_Dev.setText(data_br2());
        data_Ent.setText(data_br3());
        status.setText((String) jTable1.getValueAt(row, 6));
        mode = "UPD";

    }//GEN-LAST:event_jTable1MouseClicked
